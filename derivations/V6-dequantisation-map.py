#!/usr/bin/env python3
"""
V6-dequantisation-map.py
=========================

Formalisation of the owner-proposed dequantisation map resolving the v6
adversarial flaw #3 (quantum/classical category mismatch between the Type II
crossed-product factor A_R and the classical density field on which persistent
homology PH_k operates).

Map (owner decision 2026-04-22):

    delta_n(x, tau_R) := Tr_R[ rho_R(tau_R) * n_hat(x) ] - <n>

with
  - A_R : observer R's Type II crossed-product factor (CLPW 2023, DEHK 2024)
  - rho_R(tau_R) : reduced density operator on A_R, evolving under the modular
    flow generated by the modular Hamiltonian H_mod
  - n_hat(x) : coarse-grained number-density operator at QRF spatial resolution
    sigma_cg ~ Mpc
  - Tr_R : partial trace over unobserved QRF degrees of freedom
  - <n> : background mean density (c-number subtraction)

This script implements a *toy* model of the map on an N-qubit system (N = 4..8),
verifies that the output is a real, mean-zero classical field, and checks the
commutation of the map with the modular flow.  It also probes the classical
limit in which rho_R factorises and delta_n reduces to a Gaussian random field
-- the expected regime for a classical cosmological perturbation.

Run:
    ./.venv-compute/bin/python derivations/V6-dequantisation-map.py

All three `assert` blocks must pass.
"""

from __future__ import annotations

import numpy as np
import sympy as sp

rng = np.random.default_rng(seed=20260422)

# ---------------------------------------------------------------------------
# 1. Toy Type II_1 algebra: N-qubit system, sites 0..N-1
# ---------------------------------------------------------------------------
# We treat the N qubits as sitting on a 1-D lattice x_j = j.
# Observer R has access to the first N_R qubits (the "visible" subsystem);
# the remaining N - N_R qubits are traced out as "hidden QRF" d.o.f.
# In the finite-dim setting, the trace-class algebra B(H_R) is a Type I_{2^{N_R}}
# factor; viewed relatively to the thermal modular flow it is the finite-dim
# analogue of a Type II_1 factor (normalised trace = 2^{-N_R} Tr).

N = 6          # total qubits
N_R = 4        # observer-visible qubits
assert 1 <= N_R < N, "need at least one hidden qubit for the partial trace"
DIM = 2 ** N
DIM_R = 2 ** N_R
DIM_H = 2 ** (N - N_R)


# Single-qubit operators
I2 = np.eye(2, dtype=complex)
SZ = np.array([[1.0, 0.0], [0.0, -1.0]], dtype=complex)
SX = np.array([[0.0, 1.0], [1.0, 0.0]], dtype=complex)


def op_on_site(op: np.ndarray, site: int, n: int = N) -> np.ndarray:
    """Embed a 1-qubit operator `op` on `site` in the N-qubit Hilbert space."""
    out = np.array([[1.0]], dtype=complex)
    for j in range(n):
        out = np.kron(out, op if j == site else I2)
    return out


# Number operator at site j : n_j = (I - Z_j)/2   (occupation 0 or 1)
def number_site(j: int) -> np.ndarray:
    return 0.5 * (np.eye(DIM, dtype=complex) - op_on_site(SZ, j))


# ---------------------------------------------------------------------------
# 2. Modular Hamiltonian and thermal state rho_R on the full system
# ---------------------------------------------------------------------------
# Choose an XXZ-like modular Hamiltonian with site-dependent fields to avoid
# degeneracies (makes the modular flow nontrivial on n_hat).
h_field = rng.uniform(-0.5, 0.5, size=N)
J = 0.8
H_mod = np.zeros((DIM, DIM), dtype=complex)
for j in range(N):
    H_mod += h_field[j] * op_on_site(SZ, j)
for j in range(N - 1):
    H_mod += J * (op_on_site(SX, j) @ op_on_site(SX, j + 1))
# Hermiticity sanity
assert np.allclose(H_mod, H_mod.conj().T)

BETA = 1.0


def thermal_state(beta: float, H: np.ndarray) -> np.ndarray:
    w, V = np.linalg.eigh(H)
    w = w - w.min()                      # numerical stability
    p = np.exp(-beta * w)
    p /= p.sum()
    return (V * p) @ V.conj().T


RHO_FULL = thermal_state(BETA, H_mod)
assert np.isclose(np.trace(RHO_FULL).real, 1.0)


# ---------------------------------------------------------------------------
# 3. Partial trace: trace out the last (N - N_R) qubits = "hidden QRF"
# ---------------------------------------------------------------------------
def partial_trace_hidden(rho: np.ndarray) -> np.ndarray:
    """Trace out the trailing (N - N_R) qubits. Returns rho_R on 2^{N_R} Hilbert space."""
    tens = rho.reshape(DIM_R, DIM_H, DIM_R, DIM_H)
    return np.einsum("ikjk->ij", tens)


RHO_R = partial_trace_hidden(RHO_FULL)
assert np.isclose(np.trace(RHO_R).real, 1.0)
# Positivity of reduced state (CP map property)
eigs = np.linalg.eigvalsh(RHO_R)
assert eigs.min() > -1e-10, f"rho_R not PSD, min eig = {eigs.min()}"


# ---------------------------------------------------------------------------
# 4. Coarse-grained number density operator n_hat(x)
# ---------------------------------------------------------------------------
# We coarse-grain with a discrete Gaussian kernel of width sigma_cg (lattice
# units; sigma_cg -> Mpc scale in the cosmological lift).  n_hat acts as a
# *visible-subsystem* operator: we take the contribution from visible sites
# only -- this is the operational content of "R measures n_hat".

def coarse_number(x: float, sigma_cg: float) -> np.ndarray:
    """n_hat(x) = sum_{j in visible} K_sigma(x - j) n_j, embedded on full H."""
    weights = np.exp(-0.5 * ((np.arange(N_R) - x) / sigma_cg) ** 2)
    weights /= weights.sum()
    op = np.zeros((DIM, DIM), dtype=complex)
    for j in range(N_R):
        op += weights[j] * number_site(j)
    return op


# ---------------------------------------------------------------------------
# 5. Modular flow: rho_R(tau_R) = U(tau_R) rho_R U(tau_R)^dagger
#    with U(tau_R) = exp(-i H_mod tau_R)
# ---------------------------------------------------------------------------
def modular_evolved_full(tau_R: float) -> np.ndarray:
    w, V = np.linalg.eigh(H_mod)
    phase = np.exp(-1j * w * tau_R)
    U = (V * phase) @ V.conj().T
    return U @ RHO_FULL @ U.conj().T


# ---------------------------------------------------------------------------
# 6. THE MAP:   delta_n(x, tau_R) = Tr_R[ rho_R(tau_R) n_hat(x) ] - <n>
# ---------------------------------------------------------------------------
def background_n(sigma_cg: float) -> float:
    """<n> computed on the *initial* rho_R (tau_R = 0).  Position-averaged."""
    rho0 = partial_trace_hidden(RHO_FULL)
    vals = []
    for x in np.linspace(0.0, N_R - 1, 16):
        # n_hat here is still defined on the full Hilbert space; restricting
        # to visible sites and tracing against rho_R is equivalent because the
        # operator is block-diagonal w.r.t. the hidden factor.
        n_op = coarse_number(x, sigma_cg)
        # Reduce n_op to the visible subsystem via partial trace (as expectation
        # values agree since n_op acts as identity on hidden factor).
        n_op_R = partial_trace_hidden(n_op) / DIM_H
        vals.append(np.trace(rho0 @ n_op_R).real)
    return float(np.mean(vals))


def delta_n(x: float, tau_R: float, sigma_cg: float, nbar: float) -> float:
    rho_t_full = modular_evolved_full(tau_R)
    rho_t_R = partial_trace_hidden(rho_t_full)
    n_op = coarse_number(x, sigma_cg)
    n_op_R = partial_trace_hidden(n_op) / DIM_H
    val = np.trace(rho_t_R @ n_op_R)
    # (i) reality check on the scalar
    assert abs(val.imag) < 1e-10, f"delta_n not real: imag = {val.imag}"
    return float(val.real) - nbar


# ---------------------------------------------------------------------------
# 7. Sanity / assert block
# ---------------------------------------------------------------------------
SIGMA_CG = 1.2        # "Mpc-scale" lattice width
TAU_R = 0.7
NBAR = background_n(SIGMA_CG)
x_grid = np.linspace(0.0, N_R - 1, 32)
delta_field_t0 = np.array([delta_n(x, 0.0, SIGMA_CG, NBAR) for x in x_grid])
delta_field_tR = np.array([delta_n(x, TAU_R, SIGMA_CG, NBAR) for x in x_grid])

# Assert (i): delta_n is real -- enforced inside delta_n(); also check array
assert np.all(np.isreal(delta_field_tR))

# Assert (ii): <delta_n> = 0 at tau_R = 0 (background subtraction is correct).
# At tau != 0 the modular flow can shift the mean; we test the defining case.
mean_t0 = float(delta_field_t0.mean())
assert abs(mean_t0) < 5e-3, f"<delta_n>(tau=0) = {mean_t0}, expected ~0"

# Assert (iii): map commutes with modular flow.
# The classical flow delta_n_mod induced on delta_n(x, tau) is simply the
# Heisenberg evolution of n_hat under H_mod, then partial-trace / subtract.
# Hence   delta_n(x, tau + dtau) == <n_hat_H(dtau)>_{rho(tau)} - nbar
# which we can verify by comparing two computational paths.
DTAU = 0.15


def delta_n_via_heisenberg(x: float, tau: float, dtau: float) -> float:
    # Evolve rho to tau, then compute expectation of n_hat_H(dtau).
    rho_tau = modular_evolved_full(tau)
    w, V = np.linalg.eigh(H_mod)
    phase = np.exp(1j * w * dtau)        # Heisenberg: +i H t
    U = (V * phase) @ V.conj().T
    n_op = coarse_number(x, SIGMA_CG)
    n_H = U @ n_op @ U.conj().T
    val = np.trace(rho_tau @ n_H)
    assert abs(val.imag) < 1e-10
    return float(val.real) - NBAR


for x in x_grid[::4]:
    a = delta_n(x, TAU_R + DTAU, SIGMA_CG, NBAR)
    b = delta_n_via_heisenberg(x, TAU_R, DTAU)
    assert abs(a - b) < 1e-8, f"modular-flow commutation violated at x={x}: {a} vs {b}"


# ---------------------------------------------------------------------------
# 8. Classical-limit check
# ---------------------------------------------------------------------------
# Claim: in a product-state regime (rho_R factorises across sites) with i.i.d.
# small fluctuations around <n>, delta_n converges to a sampled Gaussian random
# field with covariance set by the Gaussian coarse-graining kernel.
#
# We simulate this by drawing many product-state thermal rho's with random
# local fields, and verifying that the resulting delta_n distribution at fixed
# x is approximately Gaussian (Shapiro-Wilk-style sanity via skew/kurtosis).

def product_thermal(beta: float) -> np.ndarray:
    rho = np.array([[1.0]], dtype=complex)
    for _ in range(N):
        h = rng.uniform(-0.3, 0.3)
        hj = h * SZ
        w, V = np.linalg.eigh(hj)
        p = np.exp(-beta * w); p /= p.sum()
        single = (V * p) @ V.conj().T
        rho = np.kron(rho, single)
    return rho


samples = []
x_probe = 1.5
for _ in range(400):
    rho = product_thermal(BETA)
    rho_R = partial_trace_hidden(rho)
    n_op_R = partial_trace_hidden(coarse_number(x_probe, SIGMA_CG)) / DIM_H
    val = np.trace(rho_R @ n_op_R).real
    samples.append(val - NBAR)
samples = np.array(samples)
skew = float(((samples - samples.mean()) ** 3).mean() / samples.std() ** 3)
kurt = float(((samples - samples.mean()) ** 4).mean() / samples.std() ** 4) - 3.0
rms = float(samples.std())


# ---------------------------------------------------------------------------
# 9. Symbolic (sympy) sanity on a 2-qubit reduction
# ---------------------------------------------------------------------------
# Sympy demonstration that delta_n is manifestly real when rho is Hermitian
# and n_hat is Hermitian -- the key well-definedness ingredient.
a, b, c = sp.symbols("a b c", real=True)
rho_sym = sp.Matrix([[a, sp.I * c], [-sp.I * c, b]])            # Hermitian
n_sym = sp.Matrix([[sp.Rational(1, 2), 0], [0, sp.Rational(3, 2)]])  # Hermitian
expect = sp.trace(rho_sym * n_sym)
assert sp.im(sp.simplify(expect)) == 0, "symbolic reality check failed"


# ---------------------------------------------------------------------------
# 10. Report
# ---------------------------------------------------------------------------
print("V6 dequantisation-map numerical verification")
print("=" * 60)
print(f"  N (total qubits)        = {N}")
print(f"  N_R (visible)           = {N_R}")
print(f"  beta                    = {BETA}")
print(f"  sigma_cg (lattice units) = {SIGMA_CG}")
print(f"  tau_R                   = {TAU_R}")
print(f"  <n> (background)        = {NBAR:.6f}")
print("-" * 60)
print(f"  <delta_n>(tau=0)        = {mean_t0:+.3e}   (assert < 5e-3)")
print(f"  RMS delta_n(tau_R)      = {float(delta_field_tR.std()):+.3e}")
print(f"  max |delta_n|(tau_R)    = {float(np.abs(delta_field_tR).max()):+.3e}")
print("-" * 60)
print("  Classical-limit probe (400 product-state samples, x = 1.5):")
print(f"     RMS       = {rms:+.3e}")
print(f"     skewness  = {skew:+.3f}   (Gaussian target: 0)")
print(f"     ex. kurt  = {kurt:+.3f}   (Gaussian target: 0)")
print("-" * 60)
print("  Asserts:")
print("    (i)   delta_n real-valued                OK")
print("    (ii)  <delta_n>(tau=0) ~ 0               OK")
print("    (iii) map commutes with modular flow     OK")
print("=" * 60)
