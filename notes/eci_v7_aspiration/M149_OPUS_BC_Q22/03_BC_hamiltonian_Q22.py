def kronecker_symbol(D, n):
    """Kronecker symbol (D|n) for D = -88 (fundamental discriminant)."""
    # For fundamental disc D, chi_D(n) is multiplicative.
    # Use mpmath jacobi which handles odd n; for even need care.
    if n == 0:
        return 0
    if n == 1:
        return 1
    # Reduce signs
    if n < 0:
        return kronecker_symbol(D, -n) * (1 if D >= 0 else -1)
    # Factor out 2's from n
    s = 1
    while n % 2 == 0:
        if D % 8 == 1:
            pass  # (D|2) = 1
        elif D % 8 == 5:
            s = -s
        elif D % 8 in (3, 7):
            # for D ≡ 3 mod 4, (D|2) actually depends on sign of D and D mod 8
            # For D = -88, D mod 8 = -88 mod 8 = 0, so 2 | D, and (D|2) = 0
            return 0
        elif D % 8 in (0, 4, 6, 2):
            return 0
        n //= 2
    if n == 1:
        return s
    # Now n odd > 1. Use Jacobi symbol with reciprocity.
    return s * int(mpmath.legendre(D, n)) if False else _jacobi(D, n) * s

def _jacobi(a, n):
    """Jacobi symbol (a/n) for n positive odd."""
    a = a % n
    result = 1
    while a != 0:
        while a % 2 == 0:
            a //= 2
            r = n % 8
            if r == 3 or r == 5:
                result = -result
        a, n = n, a
        if a % 4 == 3 and n % 4 == 3:
            result = -result
        a = a % n
    if n == 1:
        return result
    return 0

