print()
print("(F)  FRW EXTRA RESCALING -- infinitesimal form")
print("-" * 70)

# For radiation era a(eta) = eta:
# The FRW modular flow maps phi_FRW(eta_c, x) to (eta_s/eta_c) phi_FRW(eta_s, x_s).
# The infinitesimal change in the rescaling factor:
#   d/ds [a(eta_s)/a(eta_c)] |_{s=0} = (1/eta_c) * d/ds eta_s |_{s=0}
#                                      = (1/eta_c) * xi_D^eta|_{(eta_c, r)}
# At the central slice eta_c, r:
