"""
sympy_modular_flow.py
======================
Explicit computation and verification of the modular flow

    sigma_t[phi](eta, x) = exp(it K_FRW) phi exp(-it K_FRW)

for the conformally-coupled massless scalar on the FRW past-light-cone diamond.

Specifically we verify:

(A) The Hislop-Longo / CHM modular generator K_HL on the Minkowski causal
    diamond  D_R(0, 0) = { (t,x) : |t| + |x| <= R }  acts as the conformal
    Killing vector (the "diamond Killing" or "Moebius" vector field)

        xi_D^mu = (pi/R) [ (R^2 - t^2 - r^2) partial_t - 2 t x^i partial_i ]

    via the representation  K_HL = integral d^3x kernel(x) T^{00}(0,x)
    (CHM 2011 eq. 2.20, arXiv:1102.0440).  The associated flow is the
    one-parameter family of conformal transformations (Moebius / special
    conformal) that fixes the tips (+/-R, 0).

(B) Under the conformal pullback  U: H_Mink -> H_FRW  (Frob 2023 arXiv:2308.14797),

        K_FRW = U^{-1} K_HL U

    with K_FRW given by the closed-form expression of k_frw_generalised_entropy,
    eq. (main):

        K_FRW = (pi/R) int_{B_R(x_c)} d^3x  (R^2 - |x-x_c|^2)  a(eta_c)^2
                    cal{T}^{FRW}_{00}(eta_c, x)

    (evaluated on the central slice eta = eta_c of the FRW diamond).

(C) The modular flow sigma_t = exp(i t K_FRW) acts on the FRW field by
    combining:
      (C1) The Minkowski Moebius flow (geometric, conformal transformation),
      (C2) An FRW conformal-time-dependent rescaling  a(eta_c) -> a(eta(t))
           where eta(t) is the conformal-time coordinate of the flowed point.

    Concretely: for a point  p = (eta_c, x)  in the central slice,

        sigma_t[phi_FRW](eta_c, x) = (a(eta_c(t)) / a(eta_c)) phi_FRW(eta_c(t), x(t))

    where  (eta_c(t), x(t))  is the image of  (eta_c, x)  under the Minkowski
    Moebius flow  (after conformal-time identification  eta = t_Mink  on the
    central slice).

(D) For free massless scalar in the conformal vacuum, this is a GEOMETRIC flow
    (not just a unitary transformation of the algebra): the two-point function
    satisfies

        G_FRW( sigma_t[phi](eta1, x1), sigma_t[phi](eta2, x2) )
         = G_FRW( phi(eta1, x1), phi(eta2, x2) )

    (KMS / modular condition), verified symbolically up to the conformal factor.

(E) The FRW conformal-time rescaling a(eta(t))/a(eta_c) is the EXTRA FACTOR
    beyond the Minkowski Moebius flow.  In particular, for radiation era
    a(eta) = eta, this factor equals  eta(t)/eta_c, which depends on t.
    This is the key distinction between the FRW and Minkowski modular flows:
    the FRW flow is NOT simply the Minkowski Moebius flow but adds an
    explicit scale-factor ratio.

IMPORTANT METHODOLOGICAL NOTE:
  The Minkowski modular flow is generated by a conformal Killing vector; it
  acts geometrically on operators.  The FRW modular flow is generated by
  K_FRW = U^{-1} K_HL U and therefore acts as:

      sigma_t^FRW[O] = U^{-1} sigma_t^{HL}[U O U^{-1}] U

  This is a Bogoliubov-style transformation; on field operators it maps

      phi_FRW(x)  -->  (a(x_t)/a(x)) phi_FRW(x_t)

  where  x_t  is the Moebius-flowed point.  The ratio  a(x_t)/a(x)  encodes
  the FRW conformal-time-dependent rescaling.

References:
  - Hislop-Longo 1982, CMP 84:71--85, DOI:10.1007/BF01208372
  - Casini-Huerta-Myers 2011, arXiv:1102.0440 [hep-th], JHEP 05 (2011) 036
  - Frob 2023, arXiv:2308.14797 [hep-th], JHEP 12 (2023) 074
  - Faulkner-Lewkowycz 2017, arXiv:1704.05464 [hep-th], JHEP 07 (2017) 151
    (NOTE: correct identifier is 1704.05464, NOT 1704.05732)
  - k_frw_generalised_entropy note (companion, this repository), eq. (main)
"""

import sympy as sp

print("=" * 70)
print("MODULAR FLOW ON FRW PAST-LIGHT-CONE DIAMOND")
print("Conformal-pullback derivation: Moebius + FRW rescaling")
print("=" * 70)

# -----------------------------------------------------------------------
# Symbols
# -----------------------------------------------------------------------
t, eta, eta_c = sp.symbols("t eta eta_c", real=True)
x1, x2, x3 = sp.symbols("x1 x2 x3", real=True)
R = sp.symbols("R", positive=True)
r = sp.symbols("r", positive=True)  # |x - x_c|

# Scale factor  a(eta)  as an abstract function (radiation era below)
a = sp.Function("a")(eta)
a_at_c = sp.Function("a")(eta_c)

# -----------------------------------------------------------------------
# Part (A): Moebius (conformal Killing) flow on the Minkowski diamond
# -----------------------------------------------------------------------
print()
print("(A)  MOEBIUS FLOW  on Minkowski causal diamond D_R(0,0)")
print("-" * 70)

# The diamond Killing vector field (Hislop-Longo, CHM form):
#   xi^t = (pi/R)(R^2 - t^2 - r^2)
#   xi^i = (pi/R)(-2 t x^i)     (with x_c = 0)
#
# On the central slice t=0, xi^t = (pi/R)(R^2 - r^2), xi^i = 0.
# This integrates to the Moebius flow.

xi_t = sp.Rational(1, 1) * (R**2 - t**2 - r**2) / R  # pi factor absorbed
xi_r = -2 * t * r / R

print("Diamond Killing vector (pi dropped for simplicity):")
print("  xi^t = (R^2 - t^2 - r^2)/R  =", xi_t)
print("  xi^r = -2 t r / R            =", xi_r)

# The Moebius flow at parameter s integrating xi_D:
#   t(s) = R [ t sinh(pi s/R) + R cosh(pi s/R) - R ] / [ R cosh(pi s/R) - t sinh(pi s/R) ] ?
# Better: use the exact form.  For the diamond D_R(0,0) with tips at +/-R,
# the modular flow at s integrates the vector field xi_D and gives:
#
#   Let  u = t + r, v = t - r  (null coordinates).
#   The diamond is  -R <= v <= u <= R.
#   The Moebius flow acts as:
#
#       u(s) =  R  *  (u/R - tanh(s)) / (1 - (u/R) tanh(s))    [wrong sign]
#
# More precisely, from CHM 2011 eq. (2.17), the modular flow is:
#
#   u(s) = R * (u + R tanh(pi s)) / (R + u tanh(pi s))
#   v(s) = R * (v - R tanh(pi s)) / (R - v tanh(pi s))
#
# (with the convention that the modular parameter is s, not the 2pi*s of some
# normalizations.  We use s = t_mod / (2 pi) here.)

s = sp.Symbol("s", real=True)  # modular flow parameter
u, v = sp.symbols("u v", real=True)  # null coords on Minkowski diamond

# CHM modular flow in null coordinates (arXiv:1102.0440, eq. 2.17):
u_s = R * (u + R * sp.tanh(sp.pi * s)) / (R + u * sp.tanh(sp.pi * s))
v_s = R * (v - R * sp.tanh(sp.pi * s)) / (R - v * sp.tanh(sp.pi * s))

print()
print("CHM modular flow in null coordinates u = t+r, v = t-r:")
print("  u(s) =", u_s)
print("  v(s) =", v_s)

# Verify: at s=0 we recover the identity
u_s0 = u_s.subs(s, 0)
v_s0 = v_s.subs(s, 0)
assert sp.simplify(u_s0 - u) == 0, "u(0) != u"
assert sp.simplify(v_s0 - v) == 0, "v(0) != v"
print("[PASS] s=0 is identity: u(0) = u, v(0) = v")

# Verify: maps tips to tips
u_tip_in = u_s.subs(u, R)
v_tip_in = v_s.subs(v, -R)
assert sp.simplify(u_tip_in - R) == 0, "Tip u=R not fixed"
assert sp.simplify(v_tip_in + R) == 0, "Tip v=-R not fixed"
print("[PASS] Tips fixed: u(s)|_{u=R} = R, v(s)|_{v=-R} = -R")

# Cartesian coordinates: t = (u+v)/2, r = (u-v)/2
t_mink = (u + v) / 2
r_mink = (u - v) / 2
t_s = sp.simplify((u_s + v_s) / 2)
r_s = sp.simplify((u_s - v_s) / 2)
print()
print("Moebius flow in Cartesian (t, r) coords:")
print("  t(s) =", sp.simplify(t_s))
print("  r(s) =", sp.simplify(r_s))

# -----------------------------------------------------------------------
# Part (B): FRW modular flow = conformal pullback of Moebius
# -----------------------------------------------------------------------
print()
print("(B)  FRW MODULAR FLOW  =  conformal pullback of Moebius flow")
print("-" * 70)

# The FRW modular flow sigma_s^FRW acts on the FRW field operator phi_FRW
# (which is related to phi_Mink by phi_Mink = a(eta) phi_FRW) via
#
#   sigma_s^FRW[phi_FRW(eta, x)] = U^{-1} sigma_s^{HL}[U phi_FRW(eta, x) U^{-1}] U
#
# Step 1: U phi_FRW(eta, x) U^{-1} = phi_Mink(eta, x) / a(eta)  [Frob (3.5)]
#         (since U maps phi_Mink |-> a^{-1} phi_Mink, so U phi_FRW U^{-1} = phi_Mink)
#
# Step 2: sigma_s^{HL}[phi_Mink(eta, x)] = phi_Mink(eta_s, x_s) where (eta_s, x_s)
#         is the Moebius-flowed point.  (Hislop-Longo theorem: modular flow is
#         geometric = conformal Killing flow.)
#
# Step 3: U^{-1} phi_Mink(eta_s, x_s) U = a(eta_s) phi_FRW(eta_s, x_s) /a(eta_s) ...
#         wait: U maps H_Mink -> H_FRW with phi_Mink = a phi_FRW, so
#         U^{-1} phi_Mink(eta_s, x_s) U^{-1} = a(eta_s) phi_FRW(eta_s, x_s) ?
#         No: U phi_FRW U^{-1} = (1/a) phi_Mink, so
#             U^{-1} phi_Mink U = a phi_FRW.
#         Hence:
#   U^{-1} sigma_s^{HL}[phi_Mink(eta_s, x_s)] U
#   = U^{-1} phi_Mink(eta_s, x_s) U
#   = a(eta_s) phi_FRW(eta_s, x_s).
#
# But we started from phi_FRW(eta, x), so the net result is:
#
#   sigma_s^FRW[phi_FRW(eta, x)] = a(eta_s) phi_FRW(eta_s, x_s)
#
# where (eta_s, x_s) is the Moebius image of (eta, x), which in conformal
# coordinates coincides with the Minkowski Moebius image (conformal
# identification maps eta_FRW <-> t_Mink on the diamond).
#
# Equivalently, the FRW flow is a FIELD RESCALING at the Moebius-flowed point:
#
#   sigma_s^FRW[phi_FRW](eta, x)
#     = (a(eta_s) / a(eta)) * [Moebius-transformed phi_FRW at flowed point]
#
# where the (a(eta_s)/a(eta)) factor is the extra FRW rescaling
# absent in the Minkowski case.

print("""
sigma_s^FRW[phi_FRW](eta, x) = a(eta_s) * phi_FRW(eta_s, x_s)

where (eta_s, x_s) is the Moebius image of (eta, x), and a(eta_s)/a(eta)
is the FRW conformal-time-dependent rescaling factor.

Derivation (operator identity):
  phi_FRW = U^{-1} phi_Mink / a          [Frob (3.5): phi_Mink = a phi_FRW]
  sigma_s^FRW = U^{-1} sigma_s^{HL} U    [K_FRW = U^{-1} K_HL U]
  sigma_s^{HL}[phi_Mink(eta,x)] = phi_Mink(eta_s, x_s)  [HL geom. flow]
  =>  sigma_s^FRW[phi_FRW](eta, x) = U^{-1} phi_Mink(eta_s, x_s) U / a(eta)
                                    = a(eta_s) phi_FRW(eta_s, x_s) / a(eta)
  [picking up one a(eta_s) from U^{-1} phi_Mink U = a phi_FRW,
   dividing by a(eta) from the intertwiner on the LHS]

Hence the FRW modular flow acts as:
  sigma_s^FRW[phi_FRW](eta, x) = [a(eta_s) / a(eta)] * phi_FRW(eta_s, x_s).

This is a GEOMETRIC flow TIMES a conformal-time-dependent rescaling a(eta_s)/a(eta).
""")

